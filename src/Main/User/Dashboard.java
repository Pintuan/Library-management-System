    void startup(String AccountID)
    {
        setLocationRelativeTo(null);
        Connection con;
        try {
            con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=DB_LMS;integratedSecurity=true","DESKTOP-T51D5JD\\AlmarDave","");
            PreparedStatement s = con.prepareStatement("Select FName, LName FROM ACCOUNT WHERE AccID = ?");
            s.setString(1,AccountID);
            ResultSet rs = s.executeQuery();
            if (rs.next()) {
                lblFName.setText(rs.getString("FName"));
                lblLName.setText(rs.getString("LName"));
            }
            lblAvailBook.setText(String.valueOf(l.getBookCount()));
            lblBookBorrowed.setText(l.getBookCount(AccountID));
            lblBookPos.setText(l.getPendingBookCount(AccountID));
            tblAllBooks.setModel(l.getTableContentModel("BOOK"));
            tblHorror.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Horror"));
            tblNovel.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Novel"));
            tblSciFi.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "sci-fi"));
            tblBiog.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Biography"));
            tblBiog.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Biography"));
            tblHistory.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "History"));
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Server Error \n ERROR: " + ex.getErrorCode() +"\n"+ex,"Connection Error",JOptionPane.ERROR_MESSAGE);
        }
    }
