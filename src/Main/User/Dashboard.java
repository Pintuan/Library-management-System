    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        int tab = MainTab.getSelectedIndex();
        switch (tab) {
            case 0:
            tblNovel.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Novel' and BookTitle like '%"+txtSearch.getText()+"%"));
                break;
            case 1:
            tblHistory.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "History' and BookTitle like '%"+txtSearch.getText()+"%"));
                break;
            case 2:
            tblHorror.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Horror' and BookTitle like '%"+txtSearch.getText()+"%"));
                break;
            case 3:
            tblBiog.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "Biography' and BookTitle like '%"+txtSearch.getText()+"%"));
                break;
            case 4:
            tblSciFi.setModel(l.getTableContentModel(new DefaultTableModel(),"Book", "sci-fi' and BookTitle like '%"+txtSearch.getText()+"%"));
                break;
            case 5:
                tblAllBooks.setModel(l.getTableContentModel(new DefaultTableModel(),"Book","' and BookTitle like '%"+txtSearch.getText()+"%"));
                break;
        }
    }//GEN-LAST:event_txtSearchKeyReleased

